package wewe.fuding.domain;

public class Comment { 
		
		private String commentIndex;
		private String foodIndex;
		private String commentWriter;
		private String contentText;
		public String getCommentIndex() {
			return commentIndex;
		}
		public void setCommentIndex(String commentIndex) {
			this.commentIndex = commentIndex;
		}
		public String getFoodIndex() {
			return foodIndex;
		}
		public void setFoodIndex(String foodIndex) {
			this.foodIndex = foodIndex;
		}
		public String getCommentWriter() {
			return commentWriter;
		}
		public void setCommentWriter(String commentWriter) {
			this.commentWriter = commentWriter;
		}
		public String getContentText() {
			return contentText;
		}
		public void setContentText(String contentText) {
			this.contentText = contentText;
		}
		
		
}
